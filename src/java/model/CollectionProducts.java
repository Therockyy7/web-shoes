package model;

/**
 *
 * @author ACER
 */
public class CollectionProducts {
    private int productID;
    private int collectionID;
    private String name;
    private int categoryID;
    private double price;
    private String description;
    private String imageURL;
    private int stockQuantity;
    private int manufacturerID;
    private String size;
    private String gender;

    public CollectionProducts() {
    }

    public CollectionProducts(int productID, int collectionID, String name, int categoryID, double price, String description, String imageURL, int stockQuantity, int manufacturerID, String size, String gender) {
        this.productID = productID;
        this.collectionID = collectionID;
        this.name = name;
        this.categoryID = categoryID;
        this.price = price;
        this.description = description;
        this.imageURL = imageURL;
        this.stockQuantity = stockQuantity;
        this.manufacturerID = manufacturerID;
        this.size = size;
        this.gender = gender;
    }

    public int getProductID() {
        return productID;
    }

    public void setProductID(int productID) {
        this.productID = productID;
    }

    public int getCollectionID() {
        return collectionID;
    }

    public void setCollectionID(int collectionID) {
        this.collectionID = collectionID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getCategoryID() {
        return categoryID;
    }

    public void setCategoryID(int categoryID) {
        this.categoryID = categoryID;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getImageURL() {
        return imageURL;
    }

    public void setImageURL(String imageURL) {
        this.imageURL = imageURL;
    }

    public int getStockQuantity() {
        return stockQuantity;
    }

    public void setStockQuantity(int stockQuantity) {
        this.stockQuantity = stockQuantity;
    }

    public int getManufacturerID() {
        return manufacturerID;
    }

    public void setManufacturerID(int manufacturerID) {
        this.manufacturerID = manufacturerID;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }
    
    
}
